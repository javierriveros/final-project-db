  private void loadGroups() throws SQLException {
    Group.all().forEach(group -> {
      this.groupsCombo.addItem(String.format("%d- %s", group.getId(), group.getName()));
    });
  }
  
  private void loadData() {
    if (this.student == null) {
      this.incorporatioDateField.setText(now());
      return;
    }
    this.codeField.setText(String.valueOf(this.student.getCi()));
    this.namesField.setText(this.student.getName());
    this.lastNamesField.setText(this.student.getLastName());
    this.incorporatioDateField.setText(this.student.getIncorporationDate().toString());
  }

  private String now() {
    GregorianCalendar calendar = new GregorianCalendar();
    return String.format(
      "%d-%d-%d %d:%d:%d", 
      calendar.get(Calendar.YEAR), 
      calendar.get(Calendar.MONTH), 
      calendar.get(Calendar.MONTH),
      calendar.get(Calendar.HOUR_OF_DAY),
      calendar.get(Calendar.MINUTE),
      calendar.get(Calendar.SECOND)
    );
  }
  
