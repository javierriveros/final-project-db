  private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    int code = Integer.parseInt(this.codeField.getValue().toString());
    int groupId = 0;
    String name = this.namesField.getText();
    String lastName = this.lastNamesField.getText();
    String incorporationDate = this.incorporatioDateField.getText();
    
    if (!Util.hasText(this.codeField.getText()) 
        || !Util.hasText(name) 
        || !Util.hasText(lastName) 
        || !Util.hasText(incorporationDate)) {
      Util.showWarning(this, "Los datos no son válidos");
      return;
    }
    if (this.groupsCombo.getSelectedIndex() != 0)
      groupId = Util.getPKFromCombo(this.groupsCombo);
    
    Timestamp incorporationDateT = Timestamp.valueOf(incorporationDate);
    if (this.student == null) {
      this.student = new Student(code, name, lastName, incorporationDateT);
      if (groupId != 0) this.student.setGroupId(groupId);
      
      try {
        this.student.save();
        Util.showWarning(this, "Datos guardados");
        System.out.println(student);
      } catch(SQLException e) {
        Util.showWarning(this, e.getMessage());
      }
    } else {
      if (!this.student.getName().equals(name)) this.student.setName(name);
      if (!this.student.getLastName().equals(lastName)) this.student.setLastName(lastName);
      if (!this.student.getIncorporationDate().equals(incorporationDate)) this.student.setIncorporationDate(incorporationDateT);
      try {
        this.student.update();
        Util.showWarning(this, "Datos actualizados");
      } catch(SQLException e) {
        Util.showWarning(this, e.getMessage());
      }
    }
    dispose();
    parent.setEnabled(true);
  }//GEN-LAST:event_jButton2ActionPerformed

